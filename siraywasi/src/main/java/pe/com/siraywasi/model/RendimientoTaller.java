package pe.com.siraywasi.model;

public class RendimientoTaller {
	private int idRendimientoTaller;
	private int idTaller;
	private String incidencia;
	
	public int getIdRendimientoTaller() {
		return idRendimientoTaller;
	}
	public void setIdRendimientoTaller(int idRendimientoTaller) {
		this.idRendimientoTaller = idRendimientoTaller;
	}
	public int getIdTaller() {
		return idTaller;
	}
	public void setIdTaller(int idTaller) {
		this.idTaller = idTaller;
	}
	public String getIncidencia() {
		return incidencia;
	}
	public void setIncidencia(String incidencia) {
		this.incidencia = incidencia;
	}
	
}
