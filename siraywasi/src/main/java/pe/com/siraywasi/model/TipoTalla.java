package pe.com.siraywasi.model;

public class TipoTalla {
	//Estandar Niños
	private int idTipoTalla;
	private String nombreTipoTalla;
	
	public int getIdTipoTalla() {
		return idTipoTalla;
	}
	public void setIdTipoTalla(int idTipoTalla) {
		this.idTipoTalla = idTipoTalla;
	}
	public String getNombreTipoTalla() {
		return nombreTipoTalla;
	}
	public void setNombreTipoTalla(String nombreTipoTalla) {
		this.nombreTipoTalla = nombreTipoTalla;
	}
	
}
